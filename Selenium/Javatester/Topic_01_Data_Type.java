package Javatester;

public class Topic_01_Data_Type {

	public static void main(String[] args) {
	//1. khai báo trước - khởi tạo sau
		
		String fullName;
		fullName = " Mai Thị Cẩm Tiên";
		System.out.println(fullName);
		
	// 2. khai báo khởi tạo cùng lúc
		String addressName = "10 Kha van can";
		System.out.println(addressName);
		
	// Char
		char c = 'A';
		System.out.println(c);
	// byte
		byte bNumber = 55;
		int iNumber = 12345;
		short sNumber = 19000;
		long lNumber = 2000000000;
	//boolean
		boolean male = true;
	}

}
